# Escribe un programa en python que permita invertir una cadena de caracteres. El programa debe invertir la variable 'ch' que contiene la frase 'Hola Mundo'.

# Cadena de caracteres
ch = 'Hola Mundo'

# Invertir la cadena
cadena_invertida = ch[::-1]

# Mostrar la cadena invertida
print("Cadena invertida:", cadena_invertida)


# En este código, ch[::-1] se utiliza para crear una nueva cadena que es una versión invertida de la cadena original ch. La salida esperada sería "odnuM aloH".
