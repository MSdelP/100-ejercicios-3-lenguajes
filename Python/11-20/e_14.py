# Escribir las instrucciones en python que permiten crear una lista vacía L, 
# después agregarle los enteros 10, 25, 42, 69, 31, 15 y las cadenas de caracteres 'jd'. 'xa', 'rtx':

# Crear una lista vacía L
L = []

# Agregar enteros a la lista
L.append(10)
L.append(25)
L.append(42)
L.append(69)
L.append(31)

# Agregar cadenas de caracteres a la lista
L.append('jd')
L.append('xa')
L.append('rtx')

# Mostrar la lista después de agregar elementos
print("Lista después de agregar elementos:", L)


