# Escribir en python las instrucciones que permiten crear la lista L asignándole los valores [3, 2, 2, 1, 8, 1, 4, 7, 5, 1], 
# a continuación el programa debe calcular el número de apariciones del número 1 en la lista L.


# Crear la lista L
L = [3, 2, 2, 1, 8, 1, 4, 7, 5, 1]

# Calcular el número de apariciones del número 1 en la lista L
apariciones_numero_1 = L.count(1)

# Mostrar el resultado
print(f"El número de apariciones del número 1 en la lista es: {apariciones_numero_1}")


# En este código, utilizamos el método count() de las listas en Python para contar el número de apariciones del valor 1 en la lista L. 
# Luego, mostramos el resultado utilizando la función print(). La salida esperada sería el número de veces que el valor 1 aparece en la lista.
