# Escribe en python un programa que permita sumar los valores del siguiente diccionario:
# {"Manzana":15, "Banana":8, "Pera":12, "Sandía":9, "Albaricoque":3}


# Diccionario
frutas = {"Manzana": 15, "Banana": 8, "Pera": 12, "Sandía": 9, "Albaricoque": 3}

# Calcular la suma de los valores
suma_valores = sum(frutas.values())

# Mostrar la suma
print("La suma de los valores es:", suma_valores)


# Este programa utilizará la función sum() para sumar los valores del diccionario y luego mostrará la suma en consola.