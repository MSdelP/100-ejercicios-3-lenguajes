# Escribe en python un programa para generar de manera aleatoria un número entre 25 y 50.


import random

# Genera un número aleatorio entre 25 y 50 (ambos inclusive)
numero_aleatorio = random.randint(25, 50)

# Imprime el número generado
print("Número aleatorio entre 25 y 50:", numero_aleatorio)


