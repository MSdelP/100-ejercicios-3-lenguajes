function longitud(L) {
    return L.length;
}

// Ejemplo de uso
const lista_mixta = [1, 2, "tres", 4.5, [5, 6, 7], "ocho", true];
const resultado = longitud(lista_mixta);
console.log("La longitud de la lista es:", resultado);


// En este ejemplo, lista_mixta es una lista que contiene varios tipos de datos, incluyendo números, cadenas, arrays y booleanos.
// La función longitud simplemente utiliza la propiedad length de la lista para obtener el número de elementos que contiene. 
// Puedes probar la función con diferentes listas para obtener el número de elementos.