// Bucle for para imprimir los números del 1 al 20
for (let numero = 1; numero <= 20; numero++) {
    console.log(numero);
}


// Inicializar la variable para el bucle while
let numero = 1;

// Bucle while para imprimir los números del 1 al 20
while (numero <= 20) {
    console.log(numero);
    numero++;
}
