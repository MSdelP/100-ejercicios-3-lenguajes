// Bucle para imprimir la lista 10 veces
for (let i = 0; i < 10; i++) {
    // Crea la lista del 10 al 20
    let listaNumeros = Array.from({ length: 11 }, (_, index) => index + 10);

    // Imprime la lista
    console.log(listaNumeros);
}


// En JavaScript, puedes usar un bucle for para crear la lista del 10 al 20 y luego imprimir esa lista 10 veces.
// En este ejemplo, el bucle for se ejecuta 10 veces. En cada iteración del bucle, 
// se crea una lista del 10 al 20 utilizando Array.from y se imprime esa lista con console.log().
// Esto resultará en la impresión de la misma lista 10 veces.