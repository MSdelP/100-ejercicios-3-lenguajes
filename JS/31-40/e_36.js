// Crear la lista original
let L = [-4.2, 2.3, 8, -2, 6.8];

// Crear la nueva lista L1 mediante comprensión de lista
let L1 = L.filter(x => x > 0);

// Imprimir la lista L1
console.log("Lista L1:", L1);


// En este ejemplo, el método filter se utiliza para crear una nueva lista L1 que incluye solo los elementos de L que son mayores que 0.
// La lista L1 se imprime en la consola al final del programa.