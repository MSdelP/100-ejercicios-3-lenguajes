// Lista original
let L = [0, 3, 6, 9, 12, 15, 18, 21, 24, 27, 30];

// Crear la nueva lista L1 mediante una comprensión de lista
let L1 = L.map(x => x / 3);

// Imprimir la lista L1
console.log("Lista L1:", L1);


// En JavaScript, puedes crear una nueva lista L1 mediante una comprensión de lista (también conocida como expresión de mapeo) 
// que contenga los números de L divididos por 3.
// En este ejemplo, la función map se utiliza para crear una nueva lista L1 aplicando la operación de división por 3 a cada elemento de la lista L. 
// La lista L1 se imprime en la consola al final del programa.





